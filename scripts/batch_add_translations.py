def add_translations_to_file():
    """Main function to add translations"""
    print("🌍 Starting batch translation addition...")
    print(f"📁 Reading file: {TRANSLATIONS_FILE}")
    
    if not TRANSLATIONS_FILE.exists():
        print(f"❌ Error: File not found: {TRANSLATIONS_FILE}")
        sys.exit(1)
    
    content = TRANSLATIONS_FILE.read_text(encoding='utf-8')
    modified = False
    
    for lang_key, variables in LANGUAGE_VARS.items():
        print(f"\n📝 Processing {lang_key}...")
        
        # Generate the translation block for this language
        translation_block = TRANSLATION_TEMPLATE.format(**variables)
        
        # Find the position to insert (after tools.games for this language)
        # Pattern: look for the language section and its tools.games line
        pattern = rf'({lang_key}:.*?"tools\.games":\s*"[^"]+",)'
        
        match = re.search(pattern, content, re.DOTALL)
        
        if match:
            # Check if translations already exist
            if f'{lang_key}' in content and 'breathing.title' in content[match.end():match.end()+5000]:
                print(f"⚠️  Translations already exist for {lang_key}, skipping...")
                continue
            
            # Insert the translation block
            insert_pos = match.end()
            content = content[:insert_pos] + translation_block + content[insert_pos:]
            modified = True
            print(f"✅ Added translations to {lang_key}")
        else:
            print(f"❌ Could not find tools.games for {lang_key}")
    
    if modified:
        print("\n💾 Writing updated translations...")
        TRANSLATIONS_FILE.write_text(content, encoding='utf-8')
        print("✨ Translation addition complete!")
    else:
        print("\n⚠️  No changes made to the file.")


if __name__ == '__main__':
    try:
        add_translations_to_file()
    except Exception as e:
        print(f"❌ Error: {e}")
        sys.exit(1)
